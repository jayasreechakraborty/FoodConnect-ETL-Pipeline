CREATE TABLE food_waste (
    food_type VARCHAR(100),
    number_of_guests INT,
    event_type VARCHAR(100),
    quantity_of_food INT,
    storage_conditions VARCHAR(100),
    purchase_history VARCHAR(100),
    season VARCHAR(50),
    preparation_method VARCHAR(100),
    location VARCHAR(100),
    pricing VARCHAR(20),
    food_wastage_amount INT,
    food_consumed INT,
    waste_percentage DECIMAL(5,2),
    price_per_kg INT,
    revenue_loss INT,
    donation_possible VARCHAR(10),
    waste_category VARCHAR(20),
    event_size VARCHAR(20)
);


